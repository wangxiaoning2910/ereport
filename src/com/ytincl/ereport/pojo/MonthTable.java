package com.ytincl.ereport.pojo;

public class MonthTable {
	private String institudeCode;
	private String institudeName;
	private String dailyAverageQuantity;
	private String ranking;
	public String getInstitudeCode() {
		return institudeCode;
	}
	public void setInstitudeCode(String institudeCode) {
		this.institudeCode = institudeCode;
	}
	public String getInstitudeName() {
		return institudeName;
	}
	public void setInstitudeName(String institudeName) {
		this.institudeName = institudeName;
	}
	public String getDailyAverageQuantity() {
		return dailyAverageQuantity;
	}
	public void setDailyAverageQuantity(String dailyAverageQuantity) {
		this.dailyAverageQuantity = dailyAverageQuantity;
	}
	public String getRanking() {
		return ranking;
	}
	public void setRanking(String ranking) {
		this.ranking = ranking;
	}
	
}
